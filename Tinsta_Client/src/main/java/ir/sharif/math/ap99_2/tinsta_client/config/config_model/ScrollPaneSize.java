package ir.sharif.math.ap99_2.tinsta_client.config.config_model;

public class ScrollPaneSize {
    private int width;
    private int height;

    public ScrollPaneSize(int width, int height) {
        this.width = width;
        this.height = height;
    }

    public int getWidth() {
        return width;
    }

    public void setWidth(int width) {
        this.width = width;
    }

    public int getHeight() {
        return height;
    }

    public void setHeight(int height) {
        this.height = height;
    }
}
